public class Ubicaciones {
    int id, ubi_codigo;
    String ubi_descripcion, ubi_centro_costo;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getUbi_codigo() {
        return ubi_codigo;
    }

    public void setUbi_codigo(int ubi_codigo) {
        this.ubi_codigo = ubi_codigo;
    }

    public String getUbi_descripcion() {
        return ubi_descripcion;
    }

    public void setUbi_descripcion(String ubi_descripcion) {
        this.ubi_descripcion = ubi_descripcion;
    }

    public String getUbi_centro_costo() {
        return ubi_centro_costo;
    }

    public void setUbi_centro_costo(String ubi_centro_costo) {
        this.ubi_centro_costo = ubi_centro_costo;
    }
    
}

public class Fabricantes {
    int id, fab_codigo;
    String fab_nombre, fab_direccion, fab_telefono, fab_observaciones;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getFab_codigo() {
        return fab_codigo;
    }

    public void setFab_codigo(int fab_codigo) {
        this.fab_codigo = fab_codigo;
    }

    public String getFab_nombre() {
        return fab_nombre;
    }

    public void setFab_nombre(String fab_nombre) {
        this.fab_nombre = fab_nombre;
    }

    public String getFab_direccion() {
        return fab_direccion;
    }

    public void setFab_direccion(String fab_direccion) {
        this.fab_direccion = fab_direccion;
    }

    public String getFab_telefono() {
        return fab_telefono;
    }

    public void setFab_telefono(String fab_telefono) {
        this.fab_telefono = fab_telefono;
    }

    public String getFab_observaciones() {
        return fab_observaciones;
    }

    public void setFab_observaciones(String fab_observaciones) {
        this.fab_observaciones = fab_observaciones;
    }
    
}
